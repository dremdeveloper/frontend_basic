// 1. 상세한 개념
// - 변수는 데이터를 저장하는 메모리 공간의 이름이나 참조입니다. 자바스크립트에서는 var, let, const 키워드를 사용하여 변수를 선언할 수 있습니다.
// - "var"는 함수 범위(function scope) 변수를 선언하며, 현대의 자바스크립트 개발에서는 거의 사용되지 않습니다.
// - "let"은 블록 범위(block scope) 변수를 선언하며, 값을 변경할 수 있습니다.
// - "const"는 블록 범위(block scope) 상수를 선언하며, 한 번 할당하면 변경할 수 없습니다.

// 2. 개념을 충분히 이해할수 있는 코드
var x = 10; // var 키워드로 선언된 변수 (거의 사용되지 않음)
let y = 20; // let 키워드로 선언된 변수 (값 변경 가능)
const z = 30; // const 키워드로 선언된 상수 (값 변경 불가능)

console.log('x:', x); // x: 10
console.log('y:', y); // y: 20
console.log('z:', z); // z: 30

y = 25; // let으로 선언된 변수의 값 변경
console.log('updated y:', y); // updated y: 25

// z = 35; // 오류: const로 선언된 변수는 변경할 수 없습니다.

// 3. 개발자들이 많이 실수하는 부분 
// - "var"를 사용할 때 발생하는 함수 범위의 혼동, 특히 반복문 내에서의 사용이 실수를 유발할 수 있습니다.
// - "const"로 선언된 변수의 값은 변경할 수 없지만, 객체 또는 배열과 같은 참조 유형의 경우 내부 속성은 변경할 수 있습니다. 이러한 부분에서 실수가 발생할 수 있습니다.
// - 변수 선언 시 초기화를 빼먹어 undefined 에러를 발생시키는 경우가 있습니다.
